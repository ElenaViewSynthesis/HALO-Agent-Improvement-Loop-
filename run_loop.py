def extract_json(text: str) -> Any:
    text = text.strip()
    fenced = re.search(r"```(?:json)?\s*(.*?)```", text, flags=re.DOTALL)
    candidate = fenced.group(1).strip() if fenced else text
    try:
        return json.loads(candidate)
    except Exception:
        # try to heuristically find the first JSON object in the text
        m = re.search(r"({[\s\S]*})", candidate)
        if m:
            try:
                return json.loads(m.group(1))
            except Exception:
                pass
        raise

def live_flow(dry_run: bool = True):
    ensure_dirs()
    print("Creating synthetic dataroom (live)...")
    dataroom = write_workspace_dataset()
    print("Writing runtime validation tools...")
    write_runtime_tools(dataroom)

    if openai is None:
        print("OpenAI SDK not installed. Install 'openai' to run live mode.")
        return

    api_key = os.environ.get("OPENAI_API_KEY")
    if not api_key:
        print("OPENAI_API_KEY is not set. Running in dry-run mode: no model calls will be made.")
        dry_run = True

    model = os.environ.get("OPENAI_MODEL", "gpt-4o-mini")

    questions = [QUESTION_BANK[i] for i in DEFAULT_TRACE_INDICES]
    traces = []
    for index, question in enumerate(questions, start=1):
        label = f"trace-{index:02d}"
        trace_id = exported_trace_id(label)
        trace_dir = TRACE_DIR / f"trace_{index:02d}"
        outputs = trace_dir / "outputs"
        outputs.mkdir(parents=True, exist_ok=True)

        prompt = f"""
 You are a diligence analyst. Use only files inside the dataroom at path: {dataroom}.
 Return JSON only with keys: `summary_answer` (markdown), `investment_memo` (markdown),
 `risk_register` (JSON array), `open_questions` (array of strings), `citations` (array of objects),
 and `evidence_table` (CSV string). Cite files exactly by workspace-relative filename (for example `financials/p_and_l.csv`).

 Question: {question}
 """.strip()

        if dry_run:
            summary = f"# Summary Answer\n\n(DRY RUN) Answer to: {question}\n"
            memo = f"# Investment Memo\n\n(DRY RUN) {question}\n"
            risk_register = [{"id": "R-DRY-1", "risk": "demo dry-run risk", "severity": "Low", "evidence": ["financials/p_and_l.csv"]}]
            open_q = ["What is current unrestricted cash?"]
            citations = [{"claim_id": "C-DRY-1", "claim": f"Dry claim for: {question}", "sources": ["financials/p_and_l.csv"]}]
            evidence_csv = "claim_id,claim,sources\nC-DRY-1,Dry claim,financials/p_and_l.csv\n"
        else:
            print(f"Calling OpenAI for: {label} - {question}")
            # use ChatCompletion path if available, otherwise Responses API
            try:
                if hasattr(openai, "ChatCompletion"):
                    resp = openai.ChatCompletion.create(
                        model=model,
                        messages=[{"role": "user", "content": prompt}],
                        max_tokens=1500,
                        temperature=0.0,
                    )
                    text = resp["choices"][0]["message"]["content"]
                else:
                    client = openai.OpenAI(api_key=api_key)
                    resp = client.responses.create(model=model, input=prompt)
                    text = resp.output_text
                data = extract_json(text)
                summary = data.get("summary_answer") or data.get("summary") or ""
                memo = data.get("investment_memo") or ""
                risk_register = data.get("risk_register") or []
                open_q = data.get("open_questions") or []
                citations = data.get("citations") or []
                evidence_csv = data.get("evidence_table") or ""
            except Exception as exc:
                print("Model call failed or returned unexpected format:", exc)
                print("Writing partial outputs for inspection.")
                summary = f"# Summary Answer\n\n(UNPARSED MODEL OUTPUT)\n"
                memo = ""
                risk_register = []
                open_q = []
                citations = []
                evidence_csv = ""

        # write outputs
        (outputs / "summary_answer.md").write_text(summary, encoding="utf-8")
        (outputs / "investment_memo.md").write_text(memo, encoding="utf-8")
        (outputs / "risk_register.json").write_text(json.dumps(risk_register, indent=2) + "\n", encoding="utf-8")
        (outputs / "open_questions.md").write_text("\n".join([f"- {q}" for q in open_q]) + "\n", encoding="utf-8")
        (outputs / "citations.json").write_text(json.dumps(citations, indent=2) + "\n", encoding="utf-8")
        (outputs / "evidence_table.csv").write_text(evidence_csv, encoding="utf-8")

        traces.append({"trace_id": trace_id, "trace_label": label, "question": question, "answer": summary, "output_dir": str(trace_dir.relative_to(PROJECT_ROOT)), "mode": "live"})

    print(f"Live run completed: wrote {len(traces)} traces to {TRACE_DIR}")

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("command", choices=["demo", "live"], help="Which flow to run")
    parser.add_argument("--run", dest="run", action="store_true", help="When running live, actually call the model (requires OPENAI_API_KEY).")
    args = parser.parse_args()
    if args.command == "demo":
        demo_flow()
    elif args.command == "live":
        live_flow(dry_run=not bool(getattr(args, "run", False)))


if __name__ == "__main__":
    main()
