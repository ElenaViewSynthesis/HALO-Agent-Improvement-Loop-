def ensure_dirs():
    ARTIFACT_DIR.mkdir(parents=True, exist_ok=True)
    TRACE_DIR.mkdir(parents=True, exist_ok=True)
    (ARTIFACT_DIR / "halo_traces").mkdir(parents=True, exist_ok=True)


def write_workspace_dataset():
    dataroom = ARTIFACT_DIR / "synthetic_dataroom"
    if dataroom.exists():
        # remove previous demo dataset
        for p in dataroom.rglob("*"):
            try:
                if p.is_file():
                    p.unlink()
            except Exception:
                pass
    for relative_path, content in WORKSPACE_FILES.items():
        path = dataroom / relative_path
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(textwrap.dedent(content).strip() + "\n", encoding="utf-8")
    manifest = {
        "company_name": "FictionalCorp XYZ",
        "scenario": "adversarial_diligence",
        "files": sorted(str(path.relative_to(dataroom)) for path in dataroom.rglob("*") if path.is_file()),
    }
    (dataroom / "manifest.json").write_text(json.dumps(manifest, indent=2) + "\n", encoding="utf-8")
    return dataroom


def sdk_trace_id(label: str) -> str:
    return f"trace_{hashlib.sha256(label.encode('utf-8')).hexdigest()[:32]}"


def exported_trace_id(label: str) -> str:
    return sdk_trace_id(label).removeprefix("trace_")


def generate_demo_traces(dataroom: Path) -> list[dict[str, Any]]:
    traces: list[dict[str, Any]] = []
    questions = [QUESTION_BANK[i] for i in DEFAULT_TRACE_INDICES]
    for index, question in enumerate(questions, start=1):
        label = f"trace-{index:02d}"
        trace_id = exported_trace_id(label)
        trace_dir = TRACE_DIR / f"trace_{index:02d}"
        outputs = trace_dir / "outputs"
        outputs.mkdir(parents=True, exist_ok=True)
        # simple templated artifacts
        summary = f"# Summary Answer\n\nAnswer to: {question}\n\nThis is a demo-generated summary referencing `financials/p_and_l.csv`.\n"
        memo = f"# Investment Memo\n\nQuestion: {question}\n\nDemo memo content. See `risk_register.json`.\n"
        risk_register = [
            {
                "id": "R-001",
                "risk": "Demo risk - verify evidence",
                "severity": "Medium",
                "rationale": "Generated by demo pipeline.",
                "evidence": ["financials/p_and_l.csv"],
                "open_questions": ["What is current unrestricted cash?"],
            }
        ]
        open_q = "# Open Questions\n\n- What is current unrestricted cash?\n"
        citations = [
            {"claim_id": "C-001", "claim": f"Demo claim for: {question}", "sources": ["financials/p_and_l.csv"]}
        ]
        evidence_csv = "claim_id,claim,sources\nC-001,Demo claim,financials/p_and_l.csv\n"
        # write artifacts
        (outputs / "summary_answer.md").write_text(summary, encoding="utf-8")
        (outputs / "investment_memo.md").write_text(memo, encoding="utf-8")
        (outputs / "risk_register.json").write_text(json.dumps(risk_register, indent=2) + "\n", encoding="utf-8")
        (outputs / "open_questions.md").write_text(open_q, encoding="utf-8")
        (outputs / "citations.json").write_text(json.dumps(citations, indent=2) + "\n", encoding="utf-8")
        (outputs / "evidence_table.csv").write_text(evidence_csv, encoding="utf-8")
        traces.append(
            {
                "trace_id": trace_id,
                "sdk_trace_id": sdk_trace_id(label),
                "trace_label": label,
                "question": question,
                "answer": summary,
                "output_dir": str(trace_dir.relative_to(PROJECT_ROOT)),
                "mode": "demo",
            }
        )
    return traces


