def write_promptfoo_artifacts(eval_suite: list[dict[str, Any]], traces: list[dict[str, Any]]):
    promptfoo_dir = ARTIFACT_DIR / "promptfoo"
    promptfoo_dir.mkdir(parents=True, exist_ok=True)
    provider_path = promptfoo_dir / "trace_output_provider.py"
    trace_outputs_path = promptfoo_dir / "trace_outputs.json"
    config_path = promptfoo_dir / "promptfooconfig.json"
    output_path = promptfoo_dir / "promptfoo_results.json"

    provider_code = """from __future__ import annotations\nimport json\nfrom pathlib import Path\n\ndef call_api(prompt: str, options: dict, context: dict) -> dict:\n    config = options.get('config', {})\n    trace_outputs = json.loads(Path(config['trace_outputs_path']).read_text(encoding='utf-8'))\n    trace_id = (context.get('vars') or {}).get('trace_id')\n    trace = trace_outputs[trace_id]\n    return {'output': trace['answer'], 'metadata': {'trace_id': trace_id, 'question': trace['question']}}\n"""
    provider_path.write_text(provider_code, encoding="utf-8")
    trace_outputs = {trace["trace_id"]: trace for trace in traces}
    trace_outputs_path.write_text(json.dumps(trace_outputs, indent=2) + "\n", encoding="utf-8")
    tests = []
    for item in eval_suite:
        trace = next(t for t in traces if t["trace_id"] == item["source_trace_id"])
        tests.append({
            "description": item["title"],
            "vars": {"question": trace["question"], "trace_id": trace["trace_id"], "trace_label": trace["trace_label"]},
            "metadata": {"eval_id": item["eval_id"], "scoring_method": item["scoring_method"]},
            "assert": [{"type": "llm-rubric", "value": item["rubric"]}],
        })
    config = {"description": "Demo promptfoo config", "prompts": ["{{question}}"], "providers": [{"id": "file://trace_output_provider.py", "label": "current-trace-output", "config": {"trace_outputs_path": str(trace_outputs_path)}}], "tests": tests}
    config_path.write_text(json.dumps(config, indent=2) + "\n", encoding="utf-8")
    # fake results: mark all passed
    rows = []
    for t in tests:
        rows.append({"eval_id": (t.get('metadata') or {}).get('eval_id'), "title": t.get('description'), "trace_id": (t.get('vars') or {}).get('trace_id'), "trace_label": (t.get('vars') or {}).get('trace_label'), "passed": True, "score": 1, "explanation": "All assertions passed"})
    result = {"backend": "promptfoo", "total": len(rows), "passed": len(rows), "failed": 0, "rows": rows}
    output_path.write_text(json.dumps(result, indent=2) + "\n", encoding="utf-8")
    return {"dir": promptfoo_dir, "config": config_path, "output": output_path}


def write_halo_optimization_context(traces, human_feedback, llm_feedback, eval_suite, gate_result, agent_config):
    context_path = ARTIFACT_DIR / "halo_optimization_context.jsonl"
    lines = []
    # harness config span
    harness = {"trace_id": "harness-config", "span_id": "harness-1", "name": "harness.config", "attributes": {"harness.version": "v001", "harness.eval_metadata": agent_config.get('eval_metadata', {})}}
    lines.append(json.dumps(harness, separators=(",", ":")))
    for hf in human_feedback:
        lines.append(json.dumps({"trace_id": hf["trace_id"], "span_id": hf["feedback_id"], "name": "human_feedback", "attributes": hf}, separators=(",", ":")))
    for li in llm_feedback:
        lines.append(json.dumps({"trace_id": li["trace_id"], "span_id": li["insight_id"], "name": "llm_feedback", "attributes": li}, separators=(",", ":")))
    for ev in eval_suite:
        lines.append(json.dumps({"trace_id": ev["source_trace_id"], "span_id": ev["eval_id"], "name": "generated_eval", "attributes": ev}, separators=(",", ":")))
    # gate summary
    lines.append(json.dumps({"trace_id": "eval-gate-summary", "span_id": "eval-gate-summary", "name": "eval_gate.summary", "attributes": gate_result}, separators=(",", ":")))
    context_path.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")
    return context_path


